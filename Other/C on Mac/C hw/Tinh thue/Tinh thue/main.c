#include <stdio.h>

int main() {
    int n;
    do {
        printf("Input your income per year: ");
        scanf("%d", &n);
    } while (n < 0);
    
    int tax = 0;
    int m = n;
    if (m <= 48) {
        tax = 0;
    } else if (m <= 72) {
        tax = (n - 48) * 0.05;
    } else if (m <= 108) {
        tax = (n - 72) * 0.1 + (72 - 48) * 0.05;
    } else if (m <= 168) {
        tax = (n - 108) * 0.15 + (108 - 72) * 0.1 + (72 - 48) * 0.05;
    } else if (m <= 288) {
        tax = (n - 168) * 0.2 + (168 - 108) * 0.15 + (108 - 72) * 0.1 + (72 - 48) * 0.05;
    } else if (m <= 528) {
        tax = (n - 288) * 0.25 + (288 - 168) * 0.2 + (168 - 108) * 0.15 + (108 - 72) * 0.1 + (72 - 48) * 0.05;
    } else if (m <= 1008) {
        tax = (n - 528) * 0.3 + (528 - 288) * 0.25 + (288 - 168) * 0.2 + (168 - 108) * 0.15 + (108 - 72) * 0.1 + (72 - 48) * 0.05;
    } else {
        tax = (n - 1008) * 0.35 + (1008 - 528) * 0.3 + (528 - 288) * 0.25 + (288 - 168) * 0.2 + (168 - 108) * 0.15 + (108 - 72) * 0.1 + (72 - 48) * 0.05;
    }
    
    printf("Income tax per year = %d\n", tax);
    return 0;
}